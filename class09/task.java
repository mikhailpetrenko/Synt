package com.syntax.class09;

public class task {

	public static void main(String[] args) {
		for (int a=5;a>=1;a--) {
			for (int b=a;b>=1;b--) {
				System.out.print(a);
			}System.out.println();
		}
		
		System.out.println("-------------------");
		
		for (int c=1;c<=5;c++) {
			for (int d=c;d>=1;d--) {
				System.out.print("*");
			}System.out.println();}
		for (int c=1;c<=4;c++) {
			for (int d=c;d<=4;d++) {
				System.out.print("*");
			}System.out.println();
		}
	}
}

